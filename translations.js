// Variável global para armazenar o idioma atual
let currentLanguage = localStorage.getItem('language') || 'pt-BR';

// Função para obter uma tradução
function t(key) {
    return translations[currentLanguage]?.[key] || translations['pt-BR']?.[key] || key;
}

// Função para mudar o idioma
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('language', lang);
    location.reload(); // Recarregar a página para aplicar as traduções
}

// Função para traduzir a página inteira
function translatePage() {
    // Esta função será chamada quando o DOM estiver pronto
    document.querySelectorAll('[data-i18n]').forEach(element => {
        // Se o elemento tem elementos filhos com data-i18n, não sobrescrever o textContent
        const hasChildrenWithI18n = element.querySelector('[data-i18n]');
        if (!hasChildrenWithI18n) {
            element.textContent = t(element.getAttribute('data-i18n'));
        }
    });
    
    // Traduzir elementos filhos aninhados
    document.querySelectorAll('[data-i18n]').forEach(element => {
        element.childNodes.forEach(child => {
            if (child.nodeType === Node.ELEMENT_NODE && child.hasAttribute && child.hasAttribute('data-i18n')) {
                child.textContent = t(child.getAttribute('data-i18n'));
            }
        });
    });
    
    document.querySelectorAll('[data-i18n-html]').forEach(element => {
        element.innerHTML = t(element.getAttribute('data-i18n-html'));
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(element => {
        element.placeholder = t(element.getAttribute('data-i18n-placeholder'));
    });
    
    // Traduzir options em selects
    document.querySelectorAll('option[data-i18n]').forEach(option => {
        option.textContent = t(option.getAttribute('data-i18n'));
    });
}
